package day03;

public class Program02 {

	public static void main(String[] args) {
		// >, <, >=, <=, ==, !=,
		//true / false
		
		int x=10;
		int y=20;
		double z=10.0;
		double a=10.2;
		
		System.out.println(x>y);
		System.out.println(y>x);
		System.out.println(y>=y);		
		System.out.println(x<y);
		System.out.println(x==z);
		System.out.println(x==a);
		System.out.println(x!=y);
		System.out.println(x!=z);		

	}

}
