package day10;

public class Program03 {

	public static void main(String[] args) {
		
		for(int a=1;a<=5;a++)
		{
			for(int b=a;b<=5;b++)
			{
				System.out.print(b);
			}
			System.out.println();
		}
		
		for(int a=5;a>=1;a--)
		{
			for(int b=a;b<=5;b++)
			{
				System.out.print(b);
			}
			System.out.println();
		}
		

	}

}
